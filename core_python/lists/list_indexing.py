#Task: Extend this program by printing the third and fourth element in the list using Indexing.

list1 = [1, 'a', True, 2.34]
print(list1) #Output: [1, 'a', True, 2.34]
'''We are using indexing to access the first element.
Indexing is done by using square brackets [].
Indexing starts from 0.
So, the first element is at index 0.'''
x = list1[0]
print(x)    #Output: 1
'''We are using indexing to access the second element.
The second element is at index 1.'''
y = list1[1]
print(y)    #Output: a

